''' Crie um algoritmo para imprimir a sequên-
cia dos 100 primeiros números pares, usando
o For e usando o While. '''

''' ********* For ********* '''
for i in (range(2, 202,2)):
    print(i)

''' ********* While ********* '''
contador = 2
while (contador < 202):
    print(contador)
    contador = contador + 2